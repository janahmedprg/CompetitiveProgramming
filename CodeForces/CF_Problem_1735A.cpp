int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;
    for (int te = 0; te < t; te++)
    {
        ll n;
        ll a;
        ll b;
        ll c;
        cin>>n;
        n-=3;
        a = 1;
        if((n-1)%3!=0){
            b = floor((n-1)/3) + 1;
        }
        else{
            b = (n-1)/3;
        }
        if (b == 0){
            b= 1;
        }
        c = n-b-1;
 
        cout<< min(abs(a-b),min(abs(b-c),abs(c-a)))<<"\n";
 
    }
    return 0;
}